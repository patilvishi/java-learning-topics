/**
 * Day 1 - JVM, JDK, JRE Explained
 *
 * - JVM = Java Virtual Machine (runs bytecode)
 * - JRE = JVM + core libraries (to run Java apps)
 * - JDK = JRE + compiler + tools (to develop apps)
 *
 * Steps:
 * 1. Compile: javac HelloWorld.java
 * 2. Run: java HelloWorld
 */
public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello, JVM!");
    }
}
